public class ex_24 {
    public static void getNumberExplanation(int num) {
        switch (num) {
            case 666:
                System.out.println("devil number");
                break;
            case 42:
                System.out.println("answer for everything");
                break;
            case 7:
                System.out.println("prime number");
                break;
            default: System.out.println("just a number");
            break;
        }
    }
}

